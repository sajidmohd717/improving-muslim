/*
 * Generates data/transcript-index-data.js — a compact token → lecture index
 * over every caption transcript in assets/captions/. The homepage transcript
 * search loads this lazily to find which lectures mention a query, then
 * fetches only those lectures' VTT files to extract exact timestamps and
 * snippets. Transcript text itself is never duplicated into the index, so it
 * stays small as the catalog grows.
 *
 *   node scripts/generate-transcript-index.js          rewrite the index
 *   node scripts/generate-transcript-index.js --check  fail if it is stale
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { runInNewContext } from "node:vm";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const checkOnly = process.argv.includes("--check");
const OUTPUT_PATH = join(root, "data/transcript-index-data.js");

const sandbox = { window: {} };

function loadScript(relativePath) {
  const cleanPath = relativePath.replace(/^\.\//, "").split("?")[0];
  runInNewContext(readFileSync(join(root, cleanPath), "utf8"), sandbox, { filename: cleanPath });
}

loadScript("./data/series-registry.js");
for (const entry of sandbox.window.seriesConfig || []) {
  loadScript(entry.dataFile);
}
loadScript("./data/standalone-lectures-data.js");

// Same stemming as home-search.js / generate-catalog.js so query tokens and
// index tokens land on identical forms.
const STOP_WORDS = new Set(`
  the of a an in on and or for to with about from into over under between
  is are was were be been being am do does did done has have had having
  will would can could shall should may might must not no nor so if then
  than that this these those there here it its they them their he she his
  her him you your yours we our ours i me my mine who whom whose which what
  when where why how all any both each few more most other some such only
  own same too very just also even still yet again once now out up down off
  one two get got going go goes gone went come came say said know knew look
  want wanted really actually right okay yes like people time day thing
  things something someone way because
`.trim().split(/\s+/));

function stemToken(token) {
  const stem = token.replace(/(ers|ies|ing|ed|es|s)$/, "");
  return stem.length >= 4 ? stem : token;
}

function tokenize(text) {
  return String(text)
    .toLowerCase()
    .split(/[^a-z0-9']+/)
    .filter((token) => token.length >= 3 && !STOP_WORDS.has(token))
    .map(stemToken)
    .filter((token) => token.length >= 3 && !STOP_WORDS.has(token));
}

// Spoken text only: drop the header, cue numbers, timestamps, and settings.
function transcriptText(filePath) {
  return readFileSync(filePath, "utf8")
    .split(/\r?\n/)
    .filter((line) => {
      const trimmed = line.trim();
      if (!trimmed || trimmed === "WEBVTT") return false;
      if (/-->/.test(trimmed)) return false;
      if (/^\d+$/.test(trimmed)) return false;
      if (/^(NOTE|STYLE|REGION)\b/.test(trimmed)) return false;
      return true;
    })
    .join(" ");
}

const lectures = [];
const postings = new Map();

function indexLecture(key, captionsSrc) {
  if (!captionsSrc) return;
  const cleanPath = captionsSrc.replace(/^\.\//, "").split("?")[0];
  const filePath = join(root, cleanPath);
  if (!existsSync(filePath)) return;
  const tokens = new Set(tokenize(transcriptText(filePath)));
  if (!tokens.size) return;
  const lectureIndex = lectures.length;
  lectures.push({ key, captionsSrc: `./${cleanPath}` });
  for (const token of tokens) {
    if (!postings.has(token)) postings.set(token, []);
    postings.get(token).push(lectureIndex);
  }
}

for (const entry of sandbox.window.seriesConfig || []) {
  const series = sandbox.window[entry.globalKey];
  if (!series || !Array.isArray(series.episodes)) continue;
  for (const episode of series.episodes) {
    if (!episode.videoSrc) continue;
    indexLecture(`episode:${series.slug}:${episode.id}`, episode.captionsSrc);
  }
}

for (const lecture of sandbox.window.standaloneLectures || []) {
  if (!lecture.videoSrc) continue;
  indexLecture(`video:${lecture.id}`, lecture.captionsSrc);
}

const tokens = Object.fromEntries([...postings.entries()].sort((a, b) => (a[0] < b[0] ? -1 : 1)));

const banner = `/*\n * Generated by scripts/generate-transcript-index.js — do not edit by hand.\n * Regenerate with: npm run transcript-index\n */\n`;
const output = `${banner}window.transcriptIndex = ${JSON.stringify({ lectures, tokens })};\n`;

const current = existsSync(OUTPUT_PATH) ? readFileSync(OUTPUT_PATH, "utf8").replace(/\r\n/g, "\n") : null;

if (checkOnly) {
  if (current !== output) {
    console.error("data/transcript-index-data.js is missing or stale. Run: npm run transcript-index");
    process.exit(1);
  }
  console.log(`Checked transcript index (${lectures.length} transcripts, ${postings.size} tokens).`);
} else {
  if (current !== output) writeFileSync(OUTPUT_PATH, output);
  console.log(`Wrote data/transcript-index-data.js (${lectures.length} transcripts, ${postings.size} tokens).`);
}
